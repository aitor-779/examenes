
class Matricula:
    def __init__(self,numero,anyo,precio):
        self.__numero=numero
        self.__anyo=int(anyo)
        self.__precio=float(precio)
    
    def getNumero(self):
        return self.__numero 

    def getAnyo(self):
        return self.__anyo

    def getPrecio(self):
        return self.__precio 

    




    
        